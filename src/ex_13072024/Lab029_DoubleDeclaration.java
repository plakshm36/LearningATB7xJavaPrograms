package ex_13072024;

public class Lab029_DoubleDeclaration {
    public static void main(String[] args) {
        //using double simple calc program
        double s1=34;
        double s2=10;
        double sum= s1+s2;
        double sub=s1-s2;
        double mul=s1*s2;
        double div=s1/s2;
        double mod=s1%s2;


        System.out.printf("\n Result of Addition:%f",sum);
        System.out.printf("\n Result of Subtraction:%f",sub);
        System.out.printf("\n Result of Multiplication:%f",mul);
        System.out.printf("\n Result of Division:%f",div);
        System.out.println("\n Result of Modulus:"+mod);

    }


}
