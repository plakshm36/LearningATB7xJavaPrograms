package ex_13072024;

public class Lab041_Compoundassignmentoperator {
    public static void main(String[] args) {
        // Compound Assignment Operators
       // += , -=, /=, *= . %=
      int a =10;
      int b= 20;
      int c = 30;
      int d =40;
      int e = 50;
      a+=10;
      b-=10;
      c*=10;
      d/=10;
      e%=10;
      System.out.println("The result of compound assignment operator is \n"+a+","+b+","+c+","+d+","+e);

    }
}
