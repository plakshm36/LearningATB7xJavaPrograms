package ex_21072024;

public class Lab0119_ForLoopEx11 {
    public static void main(String[] args) {
        // Loop with condition
        //
        for (int i = 1; i <= 10; i++) {
            // i from 1 to 10, 10 times
            if (i == 5) {
                System.out.println("Five");
            } else {
                System.out.println(i);
            }
        }

    }
}
